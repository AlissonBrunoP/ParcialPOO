public class ProyectManager{
    private String nombrePM;
    private String apellidoPM;
    private String legajo;
    private int dni;

    public ProyectManager(String nombrePM, String apellidoPM, String legajo, int dni) {
        this.nombrePM = nombrePM;
        this.apellidoPM = apellidoPM;
        this.legajo = legajo;
        this.dni = dni;
    }
}
